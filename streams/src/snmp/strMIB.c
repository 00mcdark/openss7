	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->strMaxStrdev, &tmpint);
	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->strMaxStrmod, &tmpint);
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->strMsgPriority, &tmpint);
	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->strNband, &tmpint);
	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->strNstrmsgs, &tmpint);
	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->strNstrpush, &tmpint);
	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->strHiwat, &tmpint);
	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->strLowat, &tmpint);
