struct header_complex_index *
header_complex_add_data(struct header_complex_index **thedata, struct variable_list *var,
			void *data)
{
	struct header_complex_index *hciptrn, *hciptrp, *ourself;
	oid newoid[MAX_OID_LEN];
	size_t newoid_len;

	if (thedata == NULL || var == NULL || data == NULL)
		return NULL;

	header_complex_generate_oid(newoid, &newoid_len, NULL, 0, var);

	for (hciptrn = *thedata, hciptrp = NULL; hciptrn != NULL;
	     hciptrp = hciptrn, hciptrn = hciptrn->next)
		/* XXX: check for == and error (overlapping table entries) */
		if (snmp_oid_compare(hciptrn->name, hciptrn->namelen, newoid, newoid_len) > 0)
			break;

	/* nptr should now point to the spot that we need to add ourselves in front of, and pptr
	   should be our new 'prev'. */

	/* create ourselves */
	ourself = (struct header_complex_index *) SNMP_MALLOC_STRUCT(header_complex_index);

	/* change our pointers */
	ourself->prev = hciptrp;
	ourself->next = hciptrn;

	if (ourself->next)
		ourself->next->prev = ourself;

	if (ourself->prev)
		ourself->prev->next = ourself;

	ourself->data = data;
	ourself->name = snmp_duplicate_objid(newoid, newoid_len);
	ourself->namelen = newoid_len;

	/* free the variable list, but not the enclosed data! it's not ours! */
	snmp_free_varbind(var);

	/* rewind to the head of the list and return it (since the new head could be us, we need to 
	   notify the above routine who the head now is. */
	for (hciptrp = ourself; hciptrp->prev != NULL; hciptrp = hciptrp->prev) ;

	*thedata = hciptrp;
	DEBUGMSGTL(("header_complex_add_data", "adding something...\n"));

	return hciptrp;
}

/*
 * header_complex: - validate varbind with complex indexes
 * @datalist: pointer to data list contaning valid indexes and data
 * @vp: pointer to our prefix (if any, usually yes)
 * @name: in - varbind name, out - actual instance name considering getnext
 * @length: in - varbind name length, out - returned name length
 * @exact: whether to match exact, or next lexical ordered instance
 * @var_len: length of variable contents (simply set to sizeof(long))
 * @write_method: write method (simply set to NULL)
 */
void *
header_complex(struct header_complex_index *datalist, struct variable *vp, oid * name,
	       size_t *length, int exact, size_t *var_len, WriteMethod ** write_method)
{

	struct header_complex_index *nptr, *found = NULL;
	oid indexOid[MAX_OID_LEN];
	size_t len;
	int result;

	/* set up some nice defaults for the user */
	if (write_method)
		*write_method = NULL;
	if (var_len)
		*var_len = sizeof(long);

	for (nptr = datalist; nptr != NULL && found == NULL; nptr = nptr->next) {
		if (vp) {
			memcpy(indexOid, vp->name, vp->namelen * sizeof(oid));
			memcpy(indexOid + vp->namelen, nptr->name, nptr->namelen * sizeof(oid));
			len = vp->namelen + nptr->namelen;
		} else {
			memcpy(indexOid, nptr->name, nptr->namelen * sizeof(oid));
			len = nptr->namelen;
		}
		result = snmp_oid_compare(name, *length, indexOid, len);
		DEBUGMSGTL(("header_complex", "Checking: "));
		DEBUGMSGOID(("header_complex", indexOid, len));
		DEBUGMSG(("header_complex", "\n"));

		if (exact) {
			if (result == 0) {
				found = nptr;
			}
		} else {
			if (result == 0) {
				/* found an exact match.  Need the next one for !exact */
				if (nptr->next)
					found = nptr->next;
			} else if (result == -1) {
				found = nptr;
			}
		}
	}
	if (found) {
		if (vp) {
			memcpy(name, vp->name, vp->namelen * sizeof(oid));
			memcpy(name + vp->namelen, found->name, found->namelen * sizeof(oid));
			*length = vp->namelen + found->namelen;
		} else {
			memcpy(name, found->name, found->namelen * sizeof(oid));
			*length = found->namelen;
		}
		return found->data;
	}

	return NULL;
}

