{
	size_t tmpsize;
	struct physicalConnectionTable_data *StorageTmp = physicalConnectionTable_create();

	DEBUGMSGTL(("physicalConnectionTable", "parsing config...  "));
	if (StorageTmp == NULL) {
		config_perror("malloc failure");
		return;
	}
	/* XXX: remove individual columns if not persistent */
	line = read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->physicalEntityId, &StorageTmp->physicalEntityIdLen);
	if (StorageTmp->physicalEntityId == NULL) {
		config_perror("invalid specification for physicalEntityId");
		return;
	}
	line = read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->dataCircuitCoProtocolMachineId, &StorageTmp->dataCircuitCoProtocolMachineIdLen);
	if (StorageTmp->dataCircuitCoProtocolMachineId == NULL) {
		config_perror("invalid specification for dataCircuitCoProtocolMachineId");
		return;
	}
	line = read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->physicalConnectionId, &StorageTmp->physicalConnectionIdLen);
	if (StorageTmp->physicalConnectionId == NULL) {
		config_perror("invalid specification for physicalConnectionId");
		return;
	}
	line = read_config_read_data(ASN_OBJECT_ID, line, &StorageTmp->physicalConnectionUnderlyingConnectionNames, &StorageTmp->physicalConnectionUnderlyingConnectionNamesLen);
	if (StorageTmp->physicalConnectionUnderlyingConnectionNames == NULL) {
		config_perror("invalid specification for physicalConnectionUnderlyingConnectionNames");
		return;
	}
	line = read_config_read_data(ASN_OBJECT_ID, line, &StorageTmp->physicalConnectionSupportedConnectionNames, &StorageTmp->physicalConnectionSupportedConnectionNamesLen);
	if (StorageTmp->physicalConnectionSupportedConnectionNames == NULL) {
		config_perror("invalid specification for physicalConnectionSupportedConnectionNames");
		return;
	}
	line = read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->physicalConnectionEndpointIdentifier, &StorageTmp->physicalConnectionEndpointIdentifierLen);
	if (StorageTmp->physicalConnectionEndpointIdentifier == NULL) {
		config_perror("invalid specification for physicalConnectionEndpointIdentifier");
		return;
	}
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->physicalConnectionPortNumber, &tmpsize);
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->physicalConnectionRowStatus, &tmpsize);
	physicalConnectionTable_add(StorageTmp);
	(void) tmpsize;
	DEBUGMSGTL(("physicalConnectionTable", "done.\n"));
}

