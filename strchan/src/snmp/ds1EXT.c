{
	size_t tmpint;
	struct dsx1ConfigTable_data *StorageTmp = SNMP_MALLOC_STRUCT(dsx1ConfigTable_data);

	DEBUGMSGTL(("dsx1ConfigTable", "parsing config...  "));

	if (StorageTmp == NULL) {
		config_perror("malloc failure");
		return;
	}

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineIndex, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1IfIndex, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1TimeElapsed, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1ValidIntervals, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineType, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineCoding, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1SendCode, &tmpint);

	line =
	    read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->dsx1CircuitIdentifier,
				  &StorageTmp->dsx1CircuitIdentifierLen);
	if (StorageTmp->dsx1CircuitIdentifier == NULL) {
		config_perror("invalid specification for dsx1CircuitIdentifier");
		return;
	}

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LoopbackConfig, &tmpint);

	line =
	    read_config_read_data(ASN_BIT_STR, line, &StorageTmp->dsx1LineStatus,
				  &StorageTmp->dsx1LineStatusLen);
	if (StorageTmp->dsx1LineStatus == NULL) {
		config_perror("invalid specification for dsx1LineStatus");
		return;
	}

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1SignalMode, &tmpint);

	line =
	    read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1TransmitClockSource, &tmpint);

	line =
	    read_config_read_data(ASN_BIT_STR, line, &StorageTmp->dsx1Fdl, &StorageTmp->dsx1FdlLen);
	if (StorageTmp->dsx1Fdl == NULL) {
		config_perror("invalid specification for dsx1Fdl");
		return;
	}

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1InvalidIntervals, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineLength, &tmpint);

	line =
	    read_config_read_data(ASN_TIMETICKS, line, &StorageTmp->dsx1LineStatusLastChange,
				  &tmpint);

	line =
	    read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineStatusChangeTrapEnable,
				  &tmpint);

	line =
	    read_config_read_data(ASN_BIT_STR, line, &StorageTmp->dsx1LoopbackStatus,
				  &StorageTmp->dsx1LoopbackStatusLen);
	if (StorageTmp->dsx1LoopbackStatus == NULL) {
		config_perror("invalid specification for dsx1LoopbackStatus");
		return;
	}

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1Ds1ChannelNumber, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1Channelization, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineMode, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineBuildOut, &tmpint);

	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->dsx1LineImpedance, &tmpint);

	dsx1ConfigTable_add(StorageTmp);

	DEBUGMSGTL(("dsx1ConfigTable", "done.\n"));
}

