/**
 * @fn void deinit_rtpswOmMIB(void)
 * @brief deinitialization routine.
 *
 * This is called before the agent is unloaded.  At a minimum, deregistration of the MIB variables
 * structure (rtpswOmMIB_variables) should take place here.  By default, the function also
 * deregisters the the configuration file handlers for the MIB variables and table rows.
 *
 * Additional deregistrations that may be required here are calls to unregister_readfd(),
 * unregister_writefd() and unregsiter_exceptfd() for unhooking from the snmpd event loop, but only
 * when used as a loadable module.  By default if a read file descriptor exists, it is unregistered.
 */
void
deinit_rtpswOmMIB(void)
{
	DEBUGMSGTL(("rtpswOmMIB", "deinit_rtpswOmMIB: deinitializating...  "));
	external_signal_handler[SIGCHLD] = rtpswOmMIBold_signal_handler;
	if (my_readfd >= 0) {
		unregister_exceptfd(my_readfd);
		unregister_readfd(my_readfd);
		close(my_readfd);
		my_readfd = -1;
	}
	unregister_mib(rtpswOmMIB_variables_oid, sizeof(rtpswOmMIB_variables_oid) / sizeof(oid));
	snmpd_unregister_config_handler("rtpswOmMIB");
	snmpd_unregister_config_handler("rtpswOm5MinHistoryTable");
	snmpd_unregister_config_handler("rtpswOm15MinHistoryTable");
	snmpd_unregister_config_handler("rtpswInterfaceCurrentTable");
	snmpd_unregister_config_handler("rtpswInterface5MinHistoryTable");
	snmpd_unregister_config_handler("rtpswInterface15MinHistoryTable");
	snmpd_unregister_config_handler("rtpswIfAddrCurrentTable");
	snmpd_unregister_config_handler("rtpswIfAddr5MinHistoryTable");
	snmpd_unregister_config_handler("rtpswIfAddr15MinHistoryTable");
	snmpd_unregister_config_handler("rtpswForw5MinHistoryTable");
	snmpd_unregister_config_handler("rtpswForw15MinHistoryTable");
	snmpd_unregister_config_handler("rtpswOmTermPointCurrentTable");
	snmpd_unregister_config_handler("rtpswOmCallCurrentTable");

	/* place any other de-initialization junk you need here */
	DEBUGMSGTL(("rtpswOmMIB", "done.\n"));
}

