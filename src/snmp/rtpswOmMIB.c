{
	size_t tmpsize;
	struct rtpswOmMIB_data *StorageTmp = rtpswOmMIB_create();

	DEBUGMSGTL(("rtpswOmMIB", "parse_rtpswOmMIB: parsing config...  "));
	if (StorageTmp == NULL) {
		config_perror("malloc failure");
		return;
	}
	/* XXX: remove individual scalars that are not persistent */
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswOmCurrOctetsSent, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswOmCurrOctetsRecv, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswOmCurrPacketsSent, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswOmCurrPacketsRecv, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswOmCurrStunPacketsSent, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswOmCurrStunPacketsRecv, &tmpsize);
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->rtpswOm5minValidIntervals, &tmpsize);
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->rtpswOm15minValidIntervals, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswForwOmOctetsFor, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswForwOmOctetsRev, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswForwOmPacketsFor, &tmpsize);
	line = read_config_read_data(ASN_COUNTER64, line, &StorageTmp->rtpswForwOmPacketsRev, &tmpsize);
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->rtpswForwOm5minValidIntervals, &tmpsize);
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->rtpswForwOm15minValidIntervals, &tmpsize);
	rtpswOmMIB_add(StorageTmp);
	(void) tmpsize;
	DEBUGMSGTL(("rtpswOmMIB", "done.\n"));
}

