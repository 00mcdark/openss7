/**
 * @fn u_char * var_sfpOmMIB(struct variable *vp, oid *name, size_t *length, int exact, size_t *var_len, WriteMethod **write_method)
 * @param vp a pointer to the entry in the variables table for the requested variable.
 * @param name the object identifier for which to find.
 * @param length the length of the object identifier.
 * @param exact whether the name is exact.
 * @param var_len a pointer to the length of the representation of the object.
 * @param write_method a pointer to a write method for the object.
 * @brief locate variables in sfpOmMIB.
 *
 * This function returns a pointer to a memory area that is static across the request that contains
 * the UCD-SNMP representation of the scalar (so that it may be used to read from for a GET,
 * GET-NEXT or GET-BULK request).  This returned pointer may be NULL, in which case the function is
 * telling UCD-SNMP that the scalar does not exist for reading; however, if write_method is
 * overwritten with a non-NULL value, the function is telling UCD-SNMP that the scalar exists for
 * writing.  Write-only objects can be effected in this way.
 */
u_char *
var_sfpOmMIB(struct variable *vp, oid * name, size_t *length, int exact, size_t *var_len, WriteMethod ** write_method)
{
	struct sfpOmMIB_data *StorageTmp;
	u_char *rval;

	DEBUGMSGTL(("sfpOmMIB", "var_sfpOmMIB: lookup up varbind...  "));
	if (header_generic(vp, name, length, exact, var_len, write_method) == MATCH_FAILED)
		return NULL;
	/* Refresh the MIB values if required. */
	refresh_sfpOmMIB(0);
	if ((StorageTmp = sfpOmMIBStorage) == NULL) {
		DEBUGMSGTL(("sfpOmMIB", "no datastructure.\n"));
		return NULL;
	}
	*write_method = NULL;
	*var_len = 0;
	rval = NULL;
	/* This is where we do the value assignments for the mib results. */
	switch (vp->magic) {
	case (u_char) SFPOM5MINACTIVATE:	/* ReadWrite */
		*write_method = write_sfpOm5MinActivate;
		if (!StorageTmp)
			break;
		*var_len = StorageTmp->sfpOm5MinActivateLen * sizeof(oid);
		rval = (u_char *) StorageTmp->sfpOm5MinActivate;
		break;
	case (u_char) SFPOM5MINDEACTIVATE:	/* ReadWrite */
		*write_method = write_sfpOm5MinDeactivate;
		if (!StorageTmp)
			break;
		*var_len = StorageTmp->sfpOm5MinDeactivateLen * sizeof(oid);
		rval = (u_char *) StorageTmp->sfpOm5MinDeactivate;
		break;
	case (u_char) SFPOM15MINACTIVATE:	/* ReadWrite */
		*write_method = write_sfpOm15MinActivate;
		if (!StorageTmp)
			break;
		*var_len = StorageTmp->sfpOm15MinActivateLen * sizeof(oid);
		rval = (u_char *) StorageTmp->sfpOm15MinActivate;
		break;
	case (u_char) SFPOM15MINDEACTIVATE:	/* ReadWrite */
		*write_method = write_sfpOm15MinDeactivate;
		if (!StorageTmp)
			break;
		*var_len = StorageTmp->sfpOm15MinDeactivateLen * sizeof(oid);
		rval = (u_char *) StorageTmp->sfpOm15MinDeactivate;
		break;
	case (u_char) SFPOMDISCONTINUITYTIME:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->sfpOmDiscontinuityTime);
		rval = (u_char *) &StorageTmp->sfpOmDiscontinuityTime;
		break;
	case (u_char) SFPOMTIMESTAMP:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->sfpOmTimeStamp);
		rval = (u_char *) &StorageTmp->sfpOmTimeStamp;
		break;
	case (u_char) SFPOM5MINMAXINTERVALS:	/* ReadWrite */
		*write_method = write_sfpOm5MinMaxIntervals;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->sfpOm5MinMaxIntervals);
		rval = (u_char *) &StorageTmp->sfpOm5MinMaxIntervals;
		break;
	case (u_char) SFPOM15MINMAXINTERVALS:	/* ReadWrite */
		*write_method = write_sfpOm15MinMaxIntervals;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->sfpOm15MinMaxIntervals);
		rval = (u_char *) &StorageTmp->sfpOm15MinMaxIntervals;
		break;
	case (u_char) SFPOMPOLLINGINTERVAL:	/* ReadWrite */
		*write_method = write_sfpOmPollingInterval;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->sfpOmPollingInterval);
		rval = (u_char *) &StorageTmp->sfpOmPollingInterval;
		break;
	default:
		ERROR_MSG("");
	}
	if (rval)
		DEBUGMSGTL(("sfpOmMIB", "found.\n"));
	else
		DEBUGMSGTL(("sfpOmMIB", "not found.\n"));
	return (rval);
}

