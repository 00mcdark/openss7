	size_t tmpsize;
	struct rtpswForwTable_data *StorageTmp = rtpswForwTable_create();

	DEBUGMSGTL(("rtpswMIB", "parse_rtpswForwTable: parsing config...  "));
	if (StorageTmp == NULL) {
		config_perror("malloc failure");
		return;
	}
	/* XXX: remove individual columns if not persistent */
	line = read_config_read_data(ASN_UNSIGNED, line, &StorageTmp->rtpswForwId, &tmpsize);
	line = read_config_read_data(ASN_INTEGER, line, &StorageTmp->rtpswForwTpType, &tmpsize);
	SNMP_FREE(StorageTmp->rtpswForwTpCalling);
	line = read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->rtpswForwTpCalling, &StorageTmp->rtpswForwTpCallingLen);
	if (StorageTmp->rtpswForwTpCalling == NULL) {
		config_perror("invalid specification for rtpswForwTpCalling");
		return;
	}
	SNMP_FREE(StorageTmp->rtpswForwTpCalled);
	line = read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->rtpswForwTpCalled, &StorageTmp->rtpswForwTpCalledLen);
