/**
 * @fn void deinit_sfpMIB(void)
 * @brief deinitialization routine.
 *
 * This is called before the agent is unloaded.  At a minimum, deregistration of the MIB variables
 * structure (sfpMIB_variables) should take place here.  By default, the function also
 * deregisters the the configuration file handlers for the MIB variables and table rows.
 *
 * Additional deregistrations that may be required here are calls to unregister_readfd(),
 * unregister_writefd() and unregsiter_exceptfd() for unhooking from the snmpd event loop, but only
 * when used as a loadable module.  By default if a read file descriptor exists, it is unregistered.
 */
void
deinit_sfpMIB(void)
{
	DEBUGMSGTL(("sfpMIB", "deinit_sfpMIB: deinitializating...  "));
	external_signal_handler[SIGCHLD] = sfpMIBold_signal_handler;
	if (my_readfd >= 0) {
		unregister_exceptfd(my_readfd);
		unregister_readfd(my_readfd);
		close(my_readfd);
		my_readfd = -1;
	}
	unregister_mib(sfpMIB_variables_oid, sizeof(sfpMIB_variables_oid) / sizeof(oid));
	snmpd_unregister_config_handler("sfpMIB");
	snmpd_unregister_config_handler("sfpModuleTable");
	snmpd_unregister_config_handler("sfpApplTable");
	snmpd_unregister_config_handler("sfpApplCodeTable");
	snmpd_unregister_config_handler("sfpDiagTable");
	snmpd_unregister_config_handler("sfpCalibTable");
	snmpd_unregister_config_handler("sfpMonTable");
	snmpd_unregister_config_handler("sfpStatusTable");

	/* place any other de-initialization junk you need here */
	DEBUGMSGTL(("sfpMIB", "done.\n"));
}

