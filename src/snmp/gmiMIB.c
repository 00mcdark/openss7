/**
 * @fn void parse_subsystemTable(const char *token, char *line)
 * @param token token used within the configuration file.
 * @param line line from configuration file matching the token.
 * @brief parse configuration file for subsystemTable entries.
 *
 * This callback is called by UCD-SNMP when it parses a configuration file and finds a configuration
 * file line for the registsred token (in this case subsystemTable).  This routine is invoked by UCD-SNMP
 * to read the values of each row in the table from the configuration file.  Note that this
 * procedure may exist regardless of the persistence of the table.  If there are no configured
 * entries in the configuration table, this function will simply not be called.
 */
void
parse_subsystemTable(const char *token, char *line)
{
	size_t tmpsize;
	struct subsystemTable_data *StorageTmp = subsystemTable_create();

	DEBUGMSGTL(("gmiMIB", "parse_subsystemTable: parsing config...  "));
	if (StorageTmp == NULL) {
		config_perror("malloc failure");
		return;
	}
	/* XXX: remove individual columns if not persistent */
	SNMP_FREE(StorageTmp->subsystemId);
	line = read_config_read_data(ASN_OCTET_STR, line, &StorageTmp->subsystemId, &StorageTmp->subsystemIdLen);
	if (StorageTmp->subsystemId == NULL) {
		config_perror("invalid specification for subsystemId");
		return;
	}
	subsystemTable_add(StorageTmp);
	(void) tmpsize;
	DEBUGMSGTL(("gmiMIB", "done.\n"));
}

/*
 * store_subsystemTable(): store configuraiton file for subsystemTable
 * stores .conf file entries needed to configure the mib.
 */
int
store_subsystemTable(int majorID, int minorID, void *serverarg, void *clientarg)
{
	char line[SNMP_MAXBUF];
	char *cptr;
	size_t tmpsize;
	struct subsystemTable_data *StorageTmp;
	struct header_complex_index *hcindex;

	DEBUGMSGTL(("gmiMIB", "store_subsystemTable: storing data...  "));
	refresh_subsystemTable(1);
	(void) tmpsize;
	for (hcindex = subsystemTableStorage; hcindex != NULL; hcindex = hcindex->next) {
		StorageTmp = (struct subsystemTable_data *) hcindex->data;
		/* XXX: comment entire section if row not persistent */
		{
			memset(line, 0, sizeof(line));
			strcat(line, "subsystemTable ");
			cptr = line + strlen(line);
			(void) cptr;
			/* XXX: remove individual columns if not persistent */
			cptr = read_config_store_data(ASN_OCTET_STR, cptr, &StorageTmp->subsystemId, &StorageTmp->subsystemIdLen);
			snmpd_store_config(line);
		}
	}
	DEBUGMSGTL(("gmiMIB", "done.\n"));
	return SNMPERR_SUCCESS;
}

