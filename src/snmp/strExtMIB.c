/**
 * @fn u_char * var_strExtMIB(struct variable *vp, oid *name, size_t *length, int exact, size_t *var_len, WriteMethod **write_method)
 * @param vp a pointer to the entry in the variables table for the requested variable.
 * @param name the object identifier for which to find.
 * @param length the length of the object identifier.
 * @param exact whether the name is exact.
 * @param var_len a pointer to the length of the representation of the object.
 * @param write_method a pointer to a write method for the object.
 * @brief locate variables in strExtMIB.
 *
 * This function returns a pointer to a memory area that is static across the request that contains
 * the UCD-SNMP representation of the scalar (so that it may be used to read from for a GET,
 * GET-NEXT or GET-BULK request).  This returned pointer may be NULL, in which case the function is
 * telling UCD-SNMP that the scalar does not exist for reading; however, if write_method is
 * overwritten with a non-NULL value, the function is telling UCD-SNMP that the scalar exists for
 * writing.  Write-only objects can be effected in this way.
 */
u_char *
var_strExtMIB(struct variable *vp, oid * name, size_t *length, int exact, size_t *var_len, WriteMethod ** write_method)
{
	struct strExtMIB_data *StorageTmp;
	u_char *rval;

	DEBUGMSGTL(("strExtMIB", "var_strExtMIB: lookup up varbind...  "));
	if (header_generic(vp, name, length, exact, var_len, write_method) == MATCH_FAILED)
		return NULL;
	/* Refresh the MIB values if required. */
	refresh_strExtMIB(0);
	if ((StorageTmp = strExtMIBStorage) == NULL) {
		DEBUGMSGTL(("strExtMIB", "no datastructure.\n"));
		return NULL;
	}
	*write_method = NULL;
	*var_len = 0;
	rval = NULL;
	/* This is where we do the value assignments for the mib results. */
	switch (vp->magic) {
	case (u_char) STREXTSTRLOGRECORDNEXTINDEX:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->strExtStrlogRecordNextIndex);
		rval = (u_char *) &StorageTmp->strExtStrlogRecordNextIndex;
		break;
	case (u_char) STRNLOGARGS:	/* ReadWrite */
		*write_method = write_strNlogargs;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->strNlogargs);
		rval = (u_char *) &StorageTmp->strNlogargs;
		break;
	case (u_char) STREXTLOGMSGSIZE:	/* ReadWrite */
		*write_method = write_strExtLogMsgSize;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->strExtLogMsgSize);
		rval = (u_char *) &StorageTmp->strExtLogMsgSize;
		break;
	case (u_char) STREXTCONSOLELOG:	/* ReadWrite */
		*write_method = write_strExtConsoleLog;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->strExtConsoleLog);
		rval = (u_char *) &StorageTmp->strExtConsoleLog;
		break;
	case (u_char) STREXTERRORLOG:	/* ReadWrite */
		*write_method = write_strExtErrorLog;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->strExtErrorLog);
		rval = (u_char *) &StorageTmp->strExtErrorLog;
		break;
	case (u_char) STREXTTRACELOG:	/* ReadWrite */
		*write_method = write_strExtTraceLog;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->strExtTraceLog);
		rval = (u_char *) &StorageTmp->strExtTraceLog;
		break;
	default:
		ERROR_MSG("");
	}
	if (rval)
		DEBUGMSGTL(("strExtMIB", "found.\n"));
	else
		DEBUGMSGTL(("strExtMIB", "not found.\n"));
	return (rval);
}

