static int
strExtTraceLog_start()
{
	if (my_fd_trace < 0) {
		struct strioctl ioc;
		struct trace_ids *tid;
		struct header_complex_index *h;
		int i, count;

		for (count = 0, h = strExtTraceTableStorage; h; h = h->next) {
			struct strExtTraceTable_data *d = h->data;

			if (d->strExtTraceRowStatus == RS_ACTIVE)
				count++;
		}
		if (count == 0)
			/* does not need to be turned on */
			return SNMP_ERR_NOERROR;

		if ((tid = calloc(sizeof(*tid), count)) == NULL) {
			snmp_log_perror("write_strExtTraceLog: calloc");
			return SNMP_ERR_RESOURCEUNAVAILABLE;
		}
		for (i = 0, h = strExtTraceTableStorage; h; h = h->next) {
			struct strExtTraceTable_data *d = h->data;

			if (d->strExtTraceRowStatus == RS_ACTIVE) {
				tid[i].ti_mid = d->strExtTraceMid;
				tid[i].ti_sid = d->strExtTraceSid;
				tid[i].ti_level = d->strExtTraceLevel;
				i++;
			}
		}
		if ((my_fd_trace = open("/dev/streams/clone/log", O_RDWR | O_NONBLOCK)) < 0) {
			snmp_log_perror("write_strExtTraceLog: open(/dev/streams/clone/log)");
			SNMP_FREE(tid);
			return SNMP_ERR_GENERR;
		}
		register_readfd(my_fd_trace, strExtTraceLog_fd_handler, (void *) 0);
		register_exceptfd(my_fd_trace, strExtTraceLog_fd_handler, (void *) 1);
		ioc.ic_cmd = I_TRCLOG;
		ioc.ic_timout = 0;
		ioc.ic_len = count * sizeof(*tid);
		ioc.ic_dp = (char *) tid;
		if (ioctl(my_fd_trace, I_STR, &ioc) < 0) {
			snmp_log_perror("write_strExtTraceLog: ioctl(I_TRCLOG)");
			close(my_fd_trace);
			my_fd_trace = -1;
			SNMP_FREE(tid);
			return SNMP_ERR_GENERR;
		}
		SNMP_FREE(tid);
	}
	return SNMP_ERR_NOERROR;
}

static int
strExtTraceLog_stop()
{
	if (my_fd_trace >= 0) {
		unregister_exceptfd(my_fd_trace);
		unregister_readfd(my_fd_trace);
		close(my_fd_trace);
		my_fd_trace = -1;
	}
	return SNMP_ERR_NOERROR;
}

