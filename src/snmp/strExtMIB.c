/**
 * @fn void strExtMIB_loop_handler(int dummy)
 * @param sig signal number
 * @brief handle event loop interation.
 *
 * This function is registered so that, when operating as a module, snmpd will call it one per event
 * loop interation.  This function is called before the next requst is processed and after the
 * previous request is processed.  Two things are done here:  1) The file descriptor that is used to
 * synchronize the agent with (pseudo-)device drivers is closed.  (Another approach, instead of
 * closing each time, would be to restart a timer each time that a request is made (loop is
 * performed) and if it expires, close the file descriptor).  2) The request number is incremented.
 * Although a request is not generated for each loop of the snmp event loop, it is true that a new
 * request cannot be generated without performing a loop.  Therefore, the sa_request is not the
 * request number but it is a temporally unique identifier for a request.
 */
void
strExtMIB_loop_handler(int sig)
{
	DEBUGMSGTL(("strExtMIB", "strExtMIB_loop_handler: executing loop handler...  "));
	/* close files after each request */
	if (sa_fclose) {
		if (my_fd >= 0) {
			close(my_fd);
			my_fd = -1;
		}
	}
#if defined MASTER
	/* prepare for next request */
	sa_request++;
#endif				/* defined MASTER */
	if (external_signal_scheduled[sig] == 0)
		external_signal_scheduled[sig]--;
	if (strExtMIBold_signal_handler != NULL)
		(*strExtMIBold_signal_handler) (sig);
	DEBUGMSGTL(("strExtMIB", "done.\n"));
}

#define FLAG_ZEROPAD	(1<<0)	/* pad with zero */
#define FLAG_SIGN	(1<<1)	/* unsigned/signed long */
#define FLAG_PLUS	(1<<2)	/* show plus */
#define FLAG_SPACE	(1<<3)	/* space if plus */
#define FLAG_LEFT	(1<<4)	/* left justified */
#define FLAG_SPECIAL	(1<<5)	/* 0x */
#define FLAG_LARGE	(1<<6)	/* use 'ABCDEF' instead of 'abcdef' */

#define PROMOTE_TYPE		int
#define PROMOTE_SIZE		(sizeof(PROMOTE_TYPE))
#define PROMOTE_ALIGN(__x)	(((__x) + PROMOTE_SIZE - 1) & ~(PROMOTE_SIZE - 1))
#define PROMOTE_SIZEOF(__x)	((sizeof(__x) < PROMOTE_SIZE) ? PROMOTE_SIZE : sizeof(__x))
#define PROMOTE_ARGVAL(__type,__ptr) \
	(long long)({ \
		long long val; \
		if (sizeof(__type) < PROMOTE_SIZEOF(__type)) { \
			val = (long long)*((PROMOTE_TYPE *)(__ptr)); \
			__ptr = (typeof(__ptr))((char *)__ptr + PROMOTE_SIZE); \
		} else { \
			val = (long long)*((__type *)(__ptr)); \
			__ptr = (typeof(__ptr))((char *)__ptr + sizeof(__type)); \
		} \
		val; \
	})

