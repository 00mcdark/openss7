my $dbh = DBI->connect("DBI:SQLite:pcassign.db") or die "can't open pcassign.db";
my $sql;
$sql = q[
	CREATE TABLE IF NOT EXISTS pcassign (
		network CHARACTER(3),	-- not NULL
		cluster CHARACTER(3),	-- NULL or small network cluster
		member  CHARACTER(3),	-- NULL or beginning of point code block
		short   TEXT,		-- abbreivated company name
		company TEXT,		-- full company name
		state   CHARACTER(2),	-- or NULL for large and small networks
		place   TEXT,		-- or NULL for large and small networks
		site    TEXT		-- site or location
	);
];

$dbh->do($sql) or die $dbh->errstr;

$sql = q[
	CREATE TABLE IF NOT EXISTS pctype (
		network CHARACTER(3),
		cluster CHARACTER(3), -- or NULL for large network code
		state CHARACTER(2), -- state or ZZ (large) or XX (small)
		place TEXT
	);
];

$dbh->do($sql) or die $dbh->errstr;

$sql = 'INSERT OR FAIL INTO pcassign (network,cluster,member,short,company,state,place,site) VALUES (?,?,?,?,?,?,?,?);';

my $sth1 = $dbh->prepare($sql) or die $dbh->errstr;

$sql = 'INSERT OR FAIL INTO pctype (network,cluster,state,place) VALUES (?,?,?,?);';

my $sth2 = $dbh->prepare($sql) or die $dbh->errstr;

$dbh->begin_work;
foreach my $network (sort {$a<=>$b} keys %$pc_assignments) {
	if ($network >= 200) {
		my ($state,$place,$cluster,$block) = ('ZZ');
		my ($abbv,$name,$site) = @{$pc_assignments->{$network}};
		$sth1->execute($network,$cluster,$block,$abbv,$name,$state,$place,$site) or die $sth1->errstr;
		$sth2->execute($network,$cluster,$state,$place) or die $sth2->errstr;
	} elsif ($network < 5) {
		my ($state,$place,$block) = ('XX');
		foreach my $cluster (sort {$a<=>$b} keys %{$pc_assignments->{$network}}) {
			my ($abbv,$name,$site) = @{$pc_assignments->{$network}{$cluster}};
			$sth1->execute($network,$cluster,$block,$abbv,$name,$state,$place,$site) or die $sth1->errstr;
			$sth2->execute($network,$cluster,$state,$place) or die $sth2->errstr;
		}
	} elsif ($network == 5) {
		foreach my $cluster (sort {$a<=>$b} keys %{$pc_assignments->{$network}}) {
			my ($state,$place) = @{$pc_assignments->{$network}{$cluster}{state}} if $pc_assignments->{$network}{$cluster}{state};
			$sth2->execute($network,$cluster,$state,$place) or die $sth2->errstr;
			foreach my $block (sort {$a<=>$b} keys %{$pc_assignments->{$network}{$cluster}}) {
				next if $block eq 'state';
				my ($abbv,$name,$site) = @{$pc_assignments->{$network}{$cluster}{$block}};
				$sth1->execute($network,$cluster,$block,$abbv,$name,$state,$place,$site) or die $sth1->errstr;
			}
		}
	} else {
		die "invalid network code $network";
	}
}
$dbh->commit;

$sth1->finish; undef $sth1;
$sth2->finish; undef $sth2;
$dbh->disconnect; undef $dbh;

exit;

