refresh_m2uaMIB(void)
{
	if (m2uaMIBStorage == NULL) {
		struct m2uaMIB_data *StorageNew;

		if ((StorageNew = SNMP_MALLOC_STRUCT(m2uaMIB_data)) == NULL)
			return;
		/* Update scalar defaults as required here... */

		m2uaMIBStorage = StorageNew;
		m2uaMIB_refresh = 1;
	}
	if (m2uaMIB_refresh == 0)
		return;
	m2uaMIB_refresh = 0;
	/* Update scalars as required here... */
}

/*
 * var_m2uaMIB():
 *   This function is called every time the agent gets a request for
 *   a scalar variable that might be found within your mib section
 *   registered above.  It is up to you to do the right thing and
 *   return the correct value.
 *     You should also correct the value of "var_len" if necessary.
 *
 *   Please see the documentation for more information about writing
 *   module extensions, and check out the examples in the examples
 *   and mibII directories.
 */
uint8_t *
var_m2uaMIB(struct variable *vp, oid * name, size_t *length, int exact, size_t *var_len, WriteMethod ** write_method)
{
	struct m2uaMIB_data *StorageTmp;

	if (header_generic(vp, name, length, exact, var_len, write_method)
	    == MATCH_FAILED)
		return NULL;
	/* Refresh the MIB values if required. */
	refresh_m2uaMIB();
	if ((StorageTmp = m2uaMIBStorage) == NULL)
		return NULL;
	/* This is where we do the value assignments for the mib results. */
	switch (vp->magic) {

	case M2UAASNEXTINDEX:
		*write_method = NULL;	/* read-only */
		*var_len = sizeof(StorageTmp->m2uaAsNextIndex);
		return (uint8_t *) &StorageTmp->m2uaAsNextIndex;
	case M2UAIFNEXTINDEX:
		*write_method = NULL;	/* read-only */
		*var_len = sizeof(StorageTmp->m2uaIfNextIndex);
		return (uint8_t *) &StorageTmp->m2uaIfNextIndex;
	case M2UAASPNEXTINDEX:
		*write_method = NULL;	/* read-only */
		*var_len = sizeof(StorageTmp->m2uaAspNextIndex);
		return (uint8_t *) &StorageTmp->m2uaAspNextIndex;
	case M2UASGNEXTINDEX:
		*write_method = NULL;	/* read-only */
		*var_len = sizeof(StorageTmp->m2uaSgNextIndex);
		return (uint8_t *) &StorageTmp->m2uaSgNextIndex;

	default:
		ERROR_MSG("");
	}
	return NULL;
}

